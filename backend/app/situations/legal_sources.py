# Хосты закрытых/платных источников: WebFetch их не парсит, автопроверка ненадёжна.
# Такие ссылки помечаем «manual» и не флагаем как рассинхрон.
MANUAL_HOSTS = ("normativ.kontur.ru", "cbr.ru", "pravo.gov.ru")

_DOC_ID_RE = re.compile(r"cons_doc_(LAW_\d+)")


def extract_doc_id(url: str) -> str | None:
    """Возвращает 'LAW_XXX' из consultant.ru-ссылки, иначе None."""
    m = _DOC_ID_RE.search(url or "")
    return m.group(1) if m else None


def is_manual_source(url: str) -> bool:
    """True для закрытых источников, которые автоматикой не проверить."""
    return any(host in (url or "") for host in MANUAL_HOSTS)


def check_consistency(law: str, url: str) -> dict:
    """Сверяет: упоминает ли текст `law:` закон, на который ведёт URL.

    Возвращает {status, doc_id, expected}:
    - status='ok'       — токен закона найден в тексте law;
    - status='mismatch' — consultant-ссылка, но в тексте нет ни одного токена закона из URL;
    - status='manual'   — закрытый источник (normativ/cbr/pravo), проверка пропущена;
    - status='unknown'  — consultant-ссылка на LAW_XXX, которого нет в реестре.
    """
    if is_manual_source(url):
        return {"status": "manual", "doc_id": None, "expected": None}

    doc_id = extract_doc_id(url)
    if doc_id is None:
        return {"status": "manual", "doc_id": None, "expected": None}

    source = LEGAL_SOURCES.get(doc_id)
    if source is None:
        return {"status": "unknown", "doc_id": doc_id, "expected": None}

    law_lower = (law or "").lower()
    matched = any(tok.lower() in law_lower for tok in source["tokens"])
    return {
        "status": "ok" if matched else "mismatch",
        "doc_id": doc_id,
        "expected": source["name"],
    }
